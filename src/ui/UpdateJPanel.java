    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(txtUpdateSerial.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter serial number");
            return;
        }
        String serial = txtUpdateSerial.getText();
        int flag=0;
        
        for(Car c: list.getCarlist()){
            
            if(serial.equals(c.getSerial_no())){
                flag=1;
                JOptionPane.showMessageDialog(this, "Found car! Please update details");
                if(c.isAvailability()){
                    lblActive.setText("Currently active");
                }
                else{
                    lblActive.setText("Currently inactive");
                    
                }
                if(c.isMaintain_expiry()){
                    lblMaintainName.setText("Maintainance expired");
                }
                else{
                    lblMaintainName.setText("Maintainance not expired");
                    
                }
                txtCity.setText(c.getCity());
                lblManuname.setText(c.getManufacturer());
                lblYearname.setText(Integer.toString(c.getYear()));
                lblMaxname.setText(Integer.toString(c.getMax_seats()));
                lblMinname.setText(Integer.toString(c.getMin_seats()));
                txtModel.setText(c.getModel_no());
                return;
                
                
                
            }
            
            
        }
        
        if(flag==0){
            JOptionPane.showMessageDialog(this, "Invalid serial number!");
            return;
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

