    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String serial = txtUpdateSerial.getText();
        for(Car c: list.getCarlist()){
            
            if(serial.equals(c.getSerial_no())){
                boolean active = false;
        
                if(chkActive.isSelected()){
                    active = true;
                }
        
                if(txtCity.getText().isEmpty()){
                    JOptionPane.showMessageDialog(this, "City empty!");
                    return;
                }
                String city = txtCity.getText();
                String manufacturer = lstManufacturer.getSelectedItem().toString();
                int year = Integer.parseInt(lstYear.getSelectedItem().toString());
                int min_seats = Integer.parseInt(lstMinseats.getSelectedItem().toString());
                int max_seats = Integer.parseInt(lstMaxseats.getSelectedItem().toString());
                if(txtModel.getText().isEmpty()){
                    JOptionPane.showMessageDialog(this, "Model number empty!");
                    return;
                }
                String model = txtModel.getText();
        
                boolean maintain = false;
                if(chkMaintain.isSelected()){
                    maintain = true;
                }
        
                Date date= new Date();
                
                c.setAvailability(active);
                c.setCity(city);
                c.setMaintain_expiry(maintain);
                c.setMax_seats(max_seats);
                c.setModel_no(model);
                c.setMin_seats(min_seats);
                c.setYear(year);
                c.setManufacturer(manufacturer);
      
                c.setDate(date);
                
                JOptionPane.showMessageDialog(this, "Succesfully updated!");
                
            }
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed


